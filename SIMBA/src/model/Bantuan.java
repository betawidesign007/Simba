package model;

public class Bantuan {

    private int id_bantuan;
    private String nama_bantuan;
    private String jenis_bantuan;

    public int getId_bantuan() {
        return id_bantuan;
    }

    public void setId_bantuan(int id_bantuan) {
        this.id_bantuan = id_bantuan;
    }

    public String getNama_bantuan() {
        return nama_bantuan;
    }

    public void setNama_bantuan(String nama_bantuan) {
        this.nama_bantuan = nama_bantuan;
    }

    public String getJenis_bantuan() {
        return jenis_bantuan;
    }

    public void setJenis_bantuan(String jenis_bantuan) {
        this.jenis_bantuan = jenis_bantuan;
    }
}