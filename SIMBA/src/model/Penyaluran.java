package model;

public class Penyaluran {

    private int id_penyaluran;
    private int id_mustahik;
    private int id_bantuan;

    public int getId_penyaluran() {
        return id_penyaluran;
    }

    public void setId_penyaluran(int id_penyaluran) {
        this.id_penyaluran = id_penyaluran;
    }

    public int getId_mustahik() {
        return id_mustahik;
    }

    public void setId_mustahik(int id_mustahik) {
        this.id_mustahik = id_mustahik;
    }

    public int getId_bantuan() {
        return id_bantuan;
    }

    public void setId_bantuan(int id_bantuan) {
        this.id_bantuan = id_bantuan;
    }
}